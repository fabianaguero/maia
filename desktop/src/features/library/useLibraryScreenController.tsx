import { useT } from "../../i18n/I18nContext";
import type { LibraryScreenControllerInput } from "./libraryScreenControllerTypes";
import { buildLibraryScreenViewModel } from "./libraryScreenViewModel";
import { countMissingLibraryTracks } from "./libraryScreenToolbarRuntime";
import { useLibraryScreenState } from "./useLibraryScreenState";
import { useLibraryScreenImportActions } from "./useLibraryScreenImportActions";
import { useLibraryScreenToolbarActions } from "./useLibraryScreenToolbarActions";

export function useLibraryScreenController({
  tracks,
  playlists,
  repositories,
  baseAssets,
  newlyImportedId,
  selectedTrackId,
  selectedPlaylistId,
  selectedRepositoryId,
  selectedBaseAssetId,
  activeTab,
  onTabChange,
  trackLoading,
  repositoryLoading,
  baseAssetLoading,
  trackError,
  repositoryError,
  baseAssetError,
  onImportTrack,
  onImportRepository,
  onImportBaseAsset,
  onReanalyzeTrack,
  onRelinkTrack,
  onRelinkMissingTracks,
  onReanalyzeRepository,
  onDeleteTrack,
  onDeleteRepository,
  onSeedDemo,
  onSavePlaylist,
  onDeletePlaylist,
  onSelectTrack,
  onSelectPlaylist,
  onSelectRepository,
  onSelectBaseAsset,
  onInspectTrack,
  onInspectRepository,
  onInspectBaseAsset,
}: LibraryScreenControllerInput) {
  const t = useT();
  const {
    tab,
    handleTabChange,
    logConnections,
    logConnectionError,
    setLogConnectionError,
    showForm,
    setShowForm,
    playlistEditorOpen,
    playlistEditorId,
    playlistName,
    setPlaylistName,
    playlistTrackIds,
    openPlaylistEditor,
    resetPlaylistEditor,
    togglePlaylistTrack,
    handleSavePlaylist,
    refreshLogConnections,
  } = useLibraryScreenState({
    activeTab,
    onSavePlaylist,
    onSelectPlaylist,
    onTabChange,
    playlists,
    selectedPlaylistId,
  });

  const {
    handleImportTrack,
    handleImportRepository,
    handleImportBaseAsset,
    handleDeleteLogConnection,
  } = useLibraryScreenImportActions({
    onImportTrack,
    onImportRepository,
    onImportBaseAsset,
    refreshLogConnections,
    setShowForm,
    setLogConnectionError,
  });

  const missingTrackCount = countMissingLibraryTracks(tracks);

  const viewModel = buildLibraryScreenViewModel({
    activeTab: tab,
    showForm,
    counts: {
      tracks: tracks.length,
      repositories: repositories.length,
      logConnections: logConnections.length,
      baseAssets: baseAssets.length,
      missingTracks: missingTrackCount,
    },
    loadingState: {
      trackLoading,
      repositoryLoading,
      baseAssetLoading,
    },
    errorState: {
      trackError,
      repositoryError,
      logConnectionError,
      baseAssetError,
    },
    t,
  });

  const toolbarActions = useLibraryScreenToolbarActions({
    t,
    tab,
    showForm,
    setShowForm,
    viewModel,
    missingTrackCount,
    tracks,
    repositories,
    openPlaylistEditor,
    onSeedDemo,
    onRelinkMissingTracks,
    onDeleteTrack,
    onDeleteRepository,
  });

  return {
    t,
    tab,
    viewModel,
    toolbarActions,
    showForm,
    setShowForm,
    handleTabChange,
    handleImportTrack,
    handleImportRepository,
    handleImportBaseAsset,
    refreshLogConnections,
    logConnections,
    playlistEditorOpen,
    playlistEditorId,
    playlistName,
    setPlaylistName,
    playlistTrackIds,
    openPlaylistEditor,
    resetPlaylistEditor,
    togglePlaylistTrack,
    handleSavePlaylist,
    handleDeleteLogConnection,
    selectedTrackId,
    selectedPlaylistId,
    selectedRepositoryId,
    selectedBaseAssetId,
    newlyImportedId,
    tracks,
    playlists,
    repositories,
    baseAssets,
    onDeleteTrack,
    onInspectTrack,
    onReanalyzeTrack,
    onRelinkTrack,
    onSelectTrack,
    onDeleteRepository,
    onInspectRepository,
    onReanalyzeRepository,
    onSelectRepository,
    onInspectBaseAsset,
    onSelectBaseAsset,
    onDeletePlaylist,
    onSelectPlaylist,
  };
}
